const fruits = [ "Apple", "Banana", "Mango", "Orange"];
console.log(fruits);
fruits.push("Strawberry");	// Add at the end
console.log(fruits);
fruits.unshift("Blueberry");     // Add in the beginning
console.log(fruits);
fruits.pop();			// Remove from the end
console.log(fruits);
fruits.shift();			// Remove from the beginning
console.log(fruits);

